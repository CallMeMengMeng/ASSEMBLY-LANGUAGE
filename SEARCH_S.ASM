; SEARCH CHARECTER 'S' IN A STRING WITH 10 BYTES STARTING AT 8000H,
; PUT THE ADDRESS IN ADDR WHEN GET 'S', OTHERWISE SET ADDR 0FFFFH

DATA SEGMENT
ADDR    DW 0
DATA    ENDS

CODE SEGMENT
ASSUME	CS:CODE,DS:DATA
START:	MOV AX,DATA
        MOV DS,AX
        MOV	DI,8000H
		MOV	CX,10
		MOV	AL,'S'
		MOV	ADDR,0FFFFH
GOON:	SCASB
		LOOPNZ GOON
		JNZ	DONE
		DEC DI
		MOV ADDR,DI
DONE:	HLT
CODE	ENDS
		END	START