; PUT A STRING IN GRAPHICS ADDRESS THEN DISPLAY IT ON SCREEN

DATA	SEGMENT
	DB	'HELLO,GRAPHICS CARD!'	; A 20 WORDS STRING
DATA	ENDS

CODE	SEGMENT
ASSUME	CS:CODE,DS:DATA
START:	MOV	AX,DATA				; PUT THE ADDRESS OF DATA IN ES
		MOV	ES,AX
		
		MOV	AX,0B8A0H			; GRAPHICS ADDRESS OF MY PC IS A0000H-BFFFFH
		MOV	DS,AX				
		
		MOV	SI,0				; SET POINTER AT THE THE FIRST WORD
		MOV	BX,0A0H				; IT CAN DISPLAY 80 WORDS EACH LINE TAHT EQUALS TO 160 BYTES
		MOV	CX,14H				; SET A 20 TIMES COUNTER
AGAIN:	MOV	AL,ES:[SI]			; PUT EACH WORDS(1 BYTE) TO AL
		INC SI					; MOVE POINTER TO NEXT WORD
		MOV	AH,3CH				; DISPLAY STYLE OF WORDS. 3CH MEANS RED WORDS AND GRAY BACKGROUND 
		MOV	DS:[BX],AX			; PUT THE STYLE-WORD IN [DS*16+BX] TO DISPLAY BY GRAPHICS CARD
		INC	BX					
		INC	BX					; SKIP TO THE NEXT ADDRESS 'CAUSE THE AH BETWEEN ALs STORES WORD-STYLE
		LOOP AGAIN
		MOV	AH,4CH
		INT	21H
CODE	ENDS
		END START